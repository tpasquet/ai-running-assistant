export const MOCK_AGGREGATES: WeeklyAggregate[] = [
  ...overreachedAggregates,
  ...freshAggregates,
  ...kneePainAggregates,
  ...preCompetitionAggregates,
  ...burnoutAggregates,
];

export function getAggregatesForUser(userId: string): WeeklyAggregate[] {
  return MOCK_AGGREGATES.filter((a) => a.userId === userId).sort(
    (a, b) => a.weekStart.getTime() - b.weekStart.getTime()
  );
}

export function getLatestAggregate(userId: string): WeeklyAggregate | null {
  const userAggs = getAggregatesForUser(userId);
  return userAggs.length > 0 ? userAggs[userAggs.length - 1] : null;
}
