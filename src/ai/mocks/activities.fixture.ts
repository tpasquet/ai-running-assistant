const baseDate = new Date("2025-03-28");

function daysAgo(days: number): Date {
  const date = new Date(baseDate);
  date.setDate(date.getDate() - days);
  return date;
}

// Helper to convert pace (sec/km) to speed (m/s)
function paceToSpeed(paceSecKm: number): number {
  return 1000 / paceSecKm;
}

