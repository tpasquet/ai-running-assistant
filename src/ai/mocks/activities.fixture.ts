// EXPORTS
// ============================================================================

export const MOCK_ACTIVITIES: Activity[] = [
  ...overreachedActivities,
  ...freshActivities,
  ...kneePainActivities,
  ...preCompetitionActivities,
  ...burnoutActivities,
];

export function getActivitiesForUser(userId: string): Activity[] {
  return MOCK_ACTIVITIES.filter((a) => a.userId === userId);
}

export function getRecentActivities(
  userId: string,
  limit: number = 10
): Activity[] {
  return getActivitiesForUser(userId)
    .sort((a, b) => b.startDate.getTime() - a.startDate.getTime())
    .slice(0, limit);
}
